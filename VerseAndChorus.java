package com.jetbrains;

public class VerseAndChorus {

    public static void Chorus()
    {
        System.out.println("With a knick-knack paddywhack,\nGive the dog a bone, \nThis old man came rolling home.");
    }

    public static void main(String[] args)
    {
        System.out.println("This old man, he played one. \nHe played knick-knack on my thumb;");
        Chorus();
        System.out.println();

        System.out.println("This old man, he played two. \nHe played knick-knack on my shoe;");
        Chorus();
        System.out.println();

        System.out.println("This old man, he played three. \nHe played knick-knack on my knee;");
        Chorus();
        System.out.println();

        System.out.println("This old man, he played four. \nHe played knick-knack on my door;");
        Chorus();
        System.out.println();

        System.out.println("This old man, he played five. \nHe played knick-knack on my hive;");
        Chorus();
        System.out.println();

        System.out.println("This old man, he played aiz. \nHe played knick-knack on my sticks;");
        Chorus();
        System.out.println();
    }
}
